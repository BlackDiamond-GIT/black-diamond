def build_po() -> polib.POFile:
    po = polib.POFile()
    po.metadata = {
        'Project-Id-Version': 'Black Diamond Admin',
        'Language': 'uk',
        'Content-Type': 'text/plain; charset=UTF-8',
    }
    for msgid, msgstr in sorted(TRANSLATIONS.items(), key=lambda x: x[0].lower()):
        entry = polib.POEntry(msgid=msgid, msgstr=msgstr)
        po.append(entry)
    return po


def main() -> None:
    PO_PATH.parent.mkdir(parents=True, exist_ok=True)
    po = build_po()
    po.save(str(PO_PATH))
    po.save_as_mofile(str(MO_PATH))
    print(f'Wrote {PO_PATH} ({len(po)} strings)')
    print(f'Wrote {MO_PATH}')


if __name__ == '__main__':
    main()
